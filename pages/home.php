<p>
	Welkom op de website van <strong>GoedeDoelenPortal.nl</strong>.  
	<br />Deze portal is een centrale plek voor alle organisaties en hun bijbehorende goede doelen. Alle aangemelde organisaties worden gecontroleerd om de veiligheid te kunnen garanderen. 
</p>

Op deze website kun je;
<ul>
	<li>Informatie vinden over organisaties en hun goede doelen.</li>
	<li>Agenda van een organisatie/goed doel bekijken (evenementen en promoties).</li>
</ul>

Na registratie als gebruiker krijg je enkele handige features erbij waardoor doneren bijna een wedstrijd wordt.  De extra functionaliteiten bevatten o.a.;
<ul>
	<li>Donatie geschiedenis bekijken (datum, welk goed doel etc.)</li>
	<li>Reageren op de pagina's van organisaties en goede doelen.  Daarnaast kan ook gereageerd worden op evenementen/promotie acties.</li>
	<li>Eigen agenda bijhouden (evenementen/promoties).</li>
</ul>

Stichtingen en goede doelen kunnen gebruik maken van een betaald lidmaatschap. Na het aanschaffen van dit lidmaatschap krijg je de volgende extra functionaliteiten;
<ul>
	<li>Eigen goede doel pagina / promotie pagina.</li>
	<li>Statistieken van donaties.</li>
</ul>